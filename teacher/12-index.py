# Индексирование и срезы
# извлечение данных из последовательностей
a = [1, 2, 3, 4, 5, 6, 7]
print(a)

print(a[0])

print(a[-1])

print(a[-3])
print(a[len(a)-3])

# Срезы - возвращают подпоследовательности
print(a[:])
print(a[0:4])
print(a[:4])
print(a[4:])
print(a[4:-1])

# приращение
print(a[::2])

# приращение отрицательное
print(a[::-1])
print(a[3:4:-1])
print(a[4:0:-1])


